DROP TABLE IF EXISTS EMPLOYEE; 
CREATE TABLE EMPLOYEE  (
    ID INT(10) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    FIRSTNAME VARCHAR(100),  
    LASTNAME VARCHAR(100),
    DEPARTMENT VARCHAR(100)
);

DROP TABLE IF EXISTS EVENT;
CREATE TABLE EVENT (
    ID INT(10) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    EVENTID VARCHAR(100),    
    EVENTNAME VARCHAR(100),
    EVENTDESCRIPTION VARCHAR(100),
    EVENTDATE VARCHAR(100),
    MONTH VARCHAR(5),
    BASELOCATION VARCHAR(100),
    BENEFICIARYNAME VARCHAR(100),
    VENUEADDRESS VARCHAR(100),
    COUNCILNAME VARCHAR(100),
    PROJECT VARCHAR(100),
    CATEGORY VARCHAR(100),
    LIVESIMPACTED BIGINT,
    ACTIVITYTYPE INT,   
    STATUS VARCHAR(100)
);

DROP TABLE IF EXISTS EVENT_EMPLOYEE; 
CREATE TABLE EVENT_EMPLOYEE (
    ID INT(10) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    EVENTID VARCHAR(100),  
    EMPID VARCHAR(100),
    VOLUNTEERHOURS BIGINT,
    TRAVELHOURS BIGINT
);

DROP TABLE IF EXISTS EVENT_POC; 
CREATE TABLE EVENT_POC (
    ID INT(10) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    EVENTID VARCHAR(100),  
    POCID VARCHAR(100)
);