CREATE TABLE IF NOT EXISTS posts (
    id BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(255),
    content VARCHAR(255)
);

DROP TABLE IF EXISTS EVENT_SUMMARY;
CREATE TABLE EVENT_SUMMARY (
    ID INT(10) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    EVENTID VARCHAR(100),  
    MONTH VARCHAR(5),
    BASELOCATION VARCHAR(100),
    BENEFICIARYNAME VARCHAR(100),
    VENUEADDRESS VARCHAR(100),
    COUNCILNAME VARCHAR(100),
    PROJECT VARCHAR(100),
    CATEGORY VARCHAR(100),      
    EVENTNAME VARCHAR(100),
    EVENTDESCRIPTION VARCHAR(100),
    EVENTDATE VARCHAR(100),
    TOTALNOOFVOLUNTEERS INT,
    TOTALVOLUNTEERHOURS INT,
    TOTALTRAVELHOURS INT,
    OVERALLVOLUNTEERINGHOURS INT,
    LIVESIMPACTED BIGINT,
    ACTIVITYTYPE INT,
    STATUS VARCHAR(100),
    POCID VARCHAR(100),
    POCNAME VARCHAR(100),
    POCCONTACTNUMBER VARCHAR(100)
);

DROP TABLE IF EXISTS EVENT_INFO;
CREATE TABLE EVENT_INFO (
    ID INT(10) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    EVENTID VARCHAR(100),   
    BASELOCATION VARCHAR(100),
    BENEFICIARYNAME VARCHAR(100), 
    COUNCILNAME VARCHAR(100),     
    EVENTNAME VARCHAR(100),
    EVENTDESCRIPTION VARCHAR(100),
    EVENTDATE VARCHAR(100),
    EMPLOYEEID VARCHAR(100),
    EMPLOYEENAME VARCHAR(100),
    VOLUNTEERHOURS INT,
    TRAVELHOURS INT,
    LIVESIMPACTED INT,
    BUSINESSUNIT VARCHAR(100),
    STATUS VARCHAR(100),
    IIEPCATEGORY VARCHAR(100)    
);